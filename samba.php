<h1 class="h1-samba">HISTÓRIA DO SAMBA</h1>

  <div class="row">
    <p class="textgrupo">O samba foi introduzido no Brasil no período colonial pelos escravos africanos sendo portanto um estilo que provém da fusão entre as culturas africana e brasileira. Inicialmente, as festas de danças dos negros escravos na Bahia eram chamadas de "samba". A manifestação durante muito tempo foi considerada um estilo de música e dança criminalizado e visto com preconceito, devido às suas origens negras. Existem algumas versões acerca do nascimento do termo "samba". Uma delas afirma ser originário do termo "Zambra" ou "Zamba", oriundo da língua árabe, tendo nascido mais precisamente quando da invasão dos mouros à Península Ibérica no século VIII. Uma outra diz que é originário de um das muitas línguas africanas, possivelmente do quimbundo, onde "sam" significa "dar", e "ba" "receber" ou "coisa que cai". Ainda há uma versão que diz que a palavra samba vem de outra palavra africana, semba, que significa umbigada.</p>
